    private void nicnumKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_nicnumKeyPressed
        // TODO add your handling code here:
        String nic= nicnum.getText();
        
        int length = nic.length();
        char N =evt.getKeyChar();
      if(evt.getKeyChar()>='0'&& evt.getKeyChar()<='9')
        {
            if(length<12)
            {
                nicnum.setEditable(true);
            }
            else
            {
                nicnum.setEditable(false);
            }
        }else
        {
             if((Character.isDigit(N)||N==KeyEvent.VK_BACK_SPACE||N==KeyEvent.VK_DELETE))
        {
             nicnum.setEditable(true);
             
        }
             else
             {
                 nicnum.setEditable(false);
             }
        }
    }//GEN-LAST:event_nicnumKeyPressed

