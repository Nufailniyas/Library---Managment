    private void booknumKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_booknumKeyPressed
        // TODO add your handling code here:
        String bnum= booknum.getText();
        
        int length = bnum.length();
        char N =evt.getKeyChar();
      if(evt.getKeyChar()>='0'&& evt.getKeyChar()<='9')
        {
            if(length<5)
            {
                booknum.setEditable(true);
            }
            else
            {
                booknum.setEditable(false);
            }
        }else
        {
             if((Character.isDigit(N)||N==KeyEvent.VK_BACK_SPACE||N==KeyEvent.VK_DELETE))
        {
             booknum.setEditable(true);
             
        }
             else
             {
                  booknum.setEditable(false);
             }
        }
    }//GEN-LAST:event_booknumKeyPressed

